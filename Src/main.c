/**
 * @brief Enable ring for 1 second. Auto disable.
 *
 */
void Enable_Ring(void)
{
  
  if (!up_phone){
    ring_on=1;
    SEGGER_RTT_printf(0,"ring_main\r\n");
  }
  
}

void Send_Dial(uint8_t *num, uint8_t len)
{
  Number_len = len;
  number = num;
}

void Up_Phone(void)
{
  up_phone=1;
  if (ring_on){
    gsm_call_answer();
    ring_on=0;
  }
}

void Down_Phone(void)
{
  up_phone=0;
  gsm_unlock();
  gsm_call_end();
}

